# 不等号を使った比較と条件判断をしてみよう。(「より大きい」と「以上」の違いに注意!)
x = 10
# xが30より大きい場合に「xは30より大きいです」と出力してください
if (x > 30):
    print('xは30より大きいです')
else:
    print()


money = 500
apple_price = 200
# moneyの値がapple_priceの値以上の時、「りんごを買うことができます」と出力してください
if (money > apple_price):
    print('りんごを買うことができます')
else:
    print()
